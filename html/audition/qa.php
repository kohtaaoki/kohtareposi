<h3>よくある質問</h3>

<dl>
<dt>Ｑ．オーディションを受けるには、どうしたらいいの？</dt>
<dd>Ａ．パソコン・スマートフォンの応募フォームからのご応募、もしくはパソコン版サイトからダウンロードした指定の応募用紙に必要事項をご記入のうえ、郵送にてご応募ください。<br>
｢パソコン｣｢スマートフォン｣｢郵送｣、いずれかひとつの方法でご応募ください。</dd>

<dt>Ｑ．指定の応募用紙はどこにありますか？</dt>
<dd>Ａ．<a class="bold" href="entry.pdf" target=_blank>こちら</a> からダウンロードしてください。<br></dd>

<dt>Ｑ．応募用紙がダウンロードできません。</dt>
<dd>Ａ．応募用紙をご利用頂くにはAcrobat Readerが必要です。<br>
Acrobat Readerは、<a href="https://acrobat.adobe.com/jp/ja/acrobat/pdf-reader.html" target=_blank>こちら</a>からダウンロードできます。<br>
また、パソコン版サイトよりご利用ください。</dd>

<dt>Ｑ．応募用紙を送ってもらえますか？</dt>
<dd>Ａ．応募用紙の発送は行なっておりませんので、パソコン版サイトから応募用紙をダウンロードしてください。<br>
どうしてもダウンロード出来ない場合は、市販の履歴書に応募フォームの内容をご記入のうえ、郵送してください。</dd>

<dt>Ｑ．どんな写真を送ればよいですか？</dt>
<dd>Ａ．3ヶ月以内に撮影した、顔や体型がわかりやすい写真をお送りください。<br>
デジカメなどで撮影したスナップ写真で問題ありません。<br>
過度なメイク、プリクラ画像やアプリなどで加工した写真は、ご遠慮ください。</dd>

<dt>Ｑ．オーディションの応募にお金はかかりますか？</dt>
<dd>Ａ．オーディション参加費、選考審査費は一切かかりません。<br>
一次審査応募時の書類送付や送信にかかる通信費については、ご負担をお願いします。</dd>

<dt>Ｑ．所属にお金はかかりますか？</dt>
<dd>Ａ．所属に関しても一切お金はかかりません。</dd>

<dt>Ｑ．未成年ですが、オーディションに参加できますか？</dt>
<dd>Ａ．もちろん応募できます。<br>
ただし、親権者の同意が必要となりますので、必ず親権者の同意を得たうえでご応募ください。</dd>

<dt>Ｑ．「未成年の場合、親権者の同意が必要」とありますが、親でなくても大丈夫ですか？</dt>
<dd>Ａ．法的にあなたを保護する立場にある方であれば大丈夫です。</dd>

<dt>Ｑ．オンライン応募で未成年者が応募する場合、保護者の同意書はどうすればよいですか？</dt>
<dd>Ａ．二次審査時に同意書をいただきます。</dd>

<dt>Ｑ．色々な芸能事務所のオーディションに応募していますが、応募は可能ですか？</dt>
<dd>Ａ．同時に複数のオーディションに応募すると混乱が生じ、チャンスを逃してしまう可能性があります。<br>
オーディションの結果が出てからご応募いただくことをお勧めします。<br>
それでも応募を希望される場合は、あらかじめ状況をお伝えください。</dd>

<dt>Ｑ．他の事務所に所属中ですが、大丈夫ですか？</dt>
<dd>Ａ．特定の事務所・レコード会社と契約していない方が対象となります。</dd>

<dt>Ｑ．海外在住ですが、応募はできますか？</dt>
<dd>Ａ．日本での活動が可能であれば、ご応募いただくことは可能です。<br>
また、一次審査を通過した場合、二次審査の面接は基本的に日本国内で行なわせていただきますので、
ご了承のうえ、ご応募ください。</dd>

<dt>Ｑ．友達をオーディションに推薦してもよいですか？</dt>
<dd>Ａ．可能ですが、推薦の場合、ご本人、また未成年の場合はご家族の承諾を得てからご応募ください。</dd>

<dt>Ｑ．オーディション書類を届けたいのですが、手渡すことは可能ですか？</dt>
<dd>Ａ．オーディションを公平に期するため、｢パソコン｣｢スマートフォン｣｢郵送｣以外の応募は受け付けておりません。<br>
直接資料を持ってお越しになられても対応できませんので、ご遠慮ください。</dd>

<dt>Ｑ．応募書類、データは返却してもらえますか？</dt>
<dd>Ａ．応募書類・写真・データなどの資料は一切返却出来ません。<br>
大切な資料は、必ずコピー・複製したものをお送りください。<br>
選考終了後、個人情報保護法に基づき、当社が責任をもって破棄させていただきます。<br>
 </dd>

<dt>Ｑ．面接はどこで何を行なうのですか？</dt>
<dd>Ａ．都内の予定ですが、応募者の負担にならない場所で行ないたいと考えています。<br>保護者の同席も可能です。<br>
面接といっても堅苦しいものではなく、履歴書に沿った簡単な質疑応答、会話が主になりますので、ご安心ください。</dd>

<dt>Ｑ．学生がオーディションに合格した場合、仕事が優先ですか？</dt>
<dd>Ａ．学業優先で調整、サポートいたします。</dd>

<dt>Ｑ．オーディションの結果は、いつわかりますか？</dt>
<dd>Ａ．書類が当社に届いてから3ヶ月以内に、一次選考合格者のみに二次審査のお知らせの電話をいたします。<br>
応募書類には、必ず連絡のとれる連絡先を明記してください。<br>
応募した日から最長6ヶ月連絡がない場合は不合格となります。<br>
なお、合否のお問い合わせは、一切受け付けておりません。<br>
また、書類の到着、記入内容の変更・確認など、応募確認に関するお問い合わせもご遠慮いただいておりますので、ご了承ください。</dd>
</dl>


